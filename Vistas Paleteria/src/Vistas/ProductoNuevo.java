    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        Entity.EntityNuevoProducto entinuevoPro = new EntityNuevoProducto();
        //trim elimina los espacios en blanco
        
        entinuevoPro.setIdCodigo(Integer.parseInt(txtcodigo.getText()));
        
        entinuevoPro.setIdProveedor(Integer.parseInt(txtCodProveedor.getText()));
        
        entinuevoPro.setProducto(txtProducto.getText().trim());
        entinuevoPro.setStock(Integer.parseInt(((SpinnerNumberModel) jSStock.getModel()).getNumber().toString()));
        entinuevoPro.setPrecio(Double.parseDouble(txtPrecio.getText()));
        entinuevoPro.setDescripcion(txtDescripcion.getText().trim());
        
        if (new Ctrl.CtrlNuevoProducto().insertar(entinuevoPro)) {
        JOptionPane.showMessageDialog(null, "Se Registro Correctamente");  
        }else{
            JOptionPane.showMessageDialog(null,"No se Registro correctamente");
        }
        txtcodigo.setText("");
        txtCodProveedor.setText("");
        txtProducto.setText("");
        txtPrecio.setText("");
        txtDescripcion.setText("");
    }//GEN-LAST:event_btnGuardarActionPerformed

