    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        tblDetallesVenta.setModel(modelo);
        modelo.setColumnIdentifiers(new Object[]{"Venta","Fecha","total"});
        CtrlVenta ctrlVent = new CtrlVenta();
        ArrayList<EntityVenta> Lista
                = (ArrayList<EntityVenta>)ctrlVent.consultar();

        for (int i = 0; i < Lista.size(); i++) {
            modelo.addRow(new Object[]{Lista.get(i).getIdVenta(),
                Lista.get(i).getFecha(), 
             Lista.get(i).getTotal()});
        }
    }//GEN-LAST:event_formWindowOpened

    
    
