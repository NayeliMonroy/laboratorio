    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        Conexion con =new Conexion();
       java.sql.Connection cn = con.getConexion();
        int fila = tblEliminarNota.getSelectedRow();
        String valor=tblEliminarNota.getValueAt(fila,0).toString();
        if (fila >= 0) {
            
            try {
                java.sql.PreparedStatement ps= cn.prepareStatement
        ("DELETE FROM DetalleVentas WHERE IdVenta ='"+valor+"'");
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Dato eliminado");
            } catch (SQLException ex) {
                Logger.getLogger(EliminarProveedor.class.getName())
                        .log(Level.SEVERE, null, ex);
            }
                      
        } else {
            JOptionPane.showMessageDialog(null, "No hay datos que eliminar");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

     private void iniciarTabla (){
       tblEliminarNota.setModel(modelo);
        modelo.setColumnIdentifiers(new Object[]{"Venta", "Producto", "Cantidad","Fecha","total"});
         CtrlVenta ctrlVent = new CtrlVenta();
        ArrayList<EntityVenta> Lista
                = (ArrayList<EntityVenta>) ctrlVent.consultar();

        for (int i = 0; i < Lista.size(); i++) {
            modelo.addRow(new Object[]{Lista.get(i).getIdVenta(),
                Lista.get(i).getProducto(), Lista.get(i).getCantidad(),
                Lista.get(i).getFecha(), 
                Lista.get(i).getTotal()});
        }  
    }
    
    
