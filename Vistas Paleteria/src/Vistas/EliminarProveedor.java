    private void BtnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEliminarActionPerformed
        // TODO add your handling code here:
        Conexion con =new Conexion();
       java.sql.Connection cn = con.getConexion();
        int fila = tblELiminarProveedores.getSelectedRow();
        String valor=tblELiminarProveedores.getValueAt(fila,0).toString();
        if (fila >= 0) {
            
            try {
                java.sql.PreparedStatement ps= cn.prepareStatement
        ("DELETE FROM Proveedor WHERE IdProveedor ='"+valor+"'");
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Dato eliminado");
            } catch (SQLException ex) {
                Logger.getLogger(EliminarProveedor.class.getName())
                        .log(Level.SEVERE, null, ex);
            }
                      
        } else {
            JOptionPane.showMessageDialog(null, "No hay datos que eliminar");
        }
    }//GEN-LAST:event_BtnEliminarActionPerformed
    
    private void iniciarTabla (){
       tblELiminarProveedores.setModel(modelo);
        modelo.setColumnIdentifiers(new Object[]{"Codigo Proveedor", "Compañia", "Nombre del Proveedor","Material", 
            "Direccion","Codigo Postal", "Telefono"});
        CtrlNuevoProveedor ctrlProv = new CtrlNuevoProveedor();
        ArrayList<EntityNuevoProveedor> Lista
                = (ArrayList<EntityNuevoProveedor>) ctrlProv.consultar();

        for (int i = 0; i < Lista.size(); i++) {
            modelo.addRow(new Object[]{Lista.get(i).getIdProveedor(),
                Lista.get(i).getNombreCompañia(), Lista.get(i).getNombreProveedor(),
                Lista.get(i).getTituloProveedor(), Lista.get(i).getDireccion(),
             Lista.get(i).getCodigoPostal(), Lista.get(i).getTelefono()});
        }  
    }
    
